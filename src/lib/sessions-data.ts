/** Nhãn tuần theo trung tâm; tuần 5 = 29/6 → 5/7 (gồm cả ngày tháng 7). */
function weekNoteForDate(d: string): string {
  if (d >= "2026-06-29") return WEEK5_NOTE;
  const day = Number(d.slice(8, 10));
  if (day <= 7) return WEEK1_NOTE;
  if (day <= 14) return WEEK2_NOTE;
  if (day <= 21) return WEEK3_NOTE;
  return WEEK4_NOTE;
}

function hoursBetween(a: string, b: string): number {
  const [ah, am] = a.split(":").map(Number);
  const [bh, bm] = b.split(":").map(Number);
  return Math.round((((bh * 60 + bm) - (ah * 60 + am)) / 60) * 100) / 100;
}

